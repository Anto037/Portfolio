// Funzione per aggiornare tutti i testi
function updateLanguage(lang) {
  document.querySelectorAll('[data-i18n-key]').forEach(el => {
  const key = el.getAttribute('data-i18n-key');
  
  if(el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
    const placeholderKey = key + '.placeholder';
    if(dict[lang][placeholderKey]){
      el.placeholder = dict[lang][placeholderKey];
    }
  } else {
    if(dict[lang][key]){
      el.innerHTML = dict[lang][key];
    }
  }
});


  // Aggiorna aria-label della mail
  const mail = document.querySelector('.mail-link');
  if(mail) mail.setAttribute('aria-label', dict[lang]['mail.aria']);

  // Aggiorna immagine e alt della bandiera
  const toggle = document.getElementById('lang-toggle');
  if(toggle){
    const opposite = lang === 'it' ? 'en' : 'it';
    toggle.src = opposite === 'it' ? 'assets/logo-ITALIA.png' : 'assets/Logo-Inghilterra.svg';
    toggle.alt = dict[opposite]['lang.toggle'];
    toggle.setAttribute('aria-pressed', lang === 'en' ? 'true' : 'false');
  }

  localStorage.setItem('lang', lang);
}

// Inizializza lingua da localStorage o default 'it'
let currentLang = localStorage.getItem('lang') || 'it';
updateLanguage(currentLang);

// Toggle clic bandiera
document.getElementById('lang-toggle').addEventListener('click', () => {
  currentLang = currentLang === 'it' ? 'en' : 'it';
  updateLanguage(currentLang);
});
